/*
    + (addition)
    - (subtraction)
    * (moltiplication)
    / (division)
    ** (Exponentiation)
    % (modulus) means  "the rest of a division"
    ++ (increment) by 1
    -- (decrement) by 1
*/


let students = 30;

// students = students + 1;
students += 1;

// students = students - 1;
students -= 1;

// students = students * 2;
students *= 2;

// students = students / 2;
students /= 2;

// students = students ** 2;
students **=2;

// students = students % 4;
students %= 4;

students++;
students--;


console.log(students);